package com.playbench.winting.Itmes;

public class CompanyListItem {
    public CompanyListItem() {

    }

    String mCompanyNo;
    String mCompanyName;
    String mOwnerName;
    String mContact;
    String mCorporateNumber;

    public String getmCompanyNo() {
        return mCompanyNo;
    }

    public void setmCompanyNo(String mCompanyNo) {
        this.mCompanyNo = mCompanyNo;
    }

    public String getmCompanyName() {
        return mCompanyName;
    }

    public void setmCompanyName(String mCompanyName) {
        this.mCompanyName = mCompanyName;
    }

    public String getmOwnerName() {
        return mOwnerName;
    }

    public void setmOwnerName(String mOwnerName) {
        this.mOwnerName = mOwnerName;
    }

    public String getmContact() {
        return mContact;
    }

    public void setmContact(String mContact) {
        this.mContact = mContact;
    }

    public String getmCorporateNumber() {
        return mCorporateNumber;
    }

    public void setmCorporateNumber(String mCorporateNumber) {
        this.mCorporateNumber = mCorporateNumber;
    }
}
